package org.diverproject.util.stream.implementation.option;

public interface StreamOption
{
	/**
	 * Código da opção do tipo byte.
	 */
	public static final byte OPTION_BYTE = 0;

	/**
	 * Código da opção do tipo character.
	 */
	public static final byte OPTION_CHAR = 1;

	/**
	 * Código da opção do tipo short.
	 */
	public static final byte OPTION_SHORT = 2;

	/**
	 * Código da opção do tipo integer.
	 */
	public static final byte OPTION_INT = 3;

	/**
	 * Código da opção do tipo long.
	 */
	public static final byte OPTION_LONG = 4;

	/**
	 * Código da opção do tipo float.
	 */
	public static final byte OPTION_FLOAT = 5;

	/**
	 * Código da opção do tipo double.
	 */
	public static final byte OPTION_DOUBLE = 6;

	/**
	 * Código da opção do tipo string.
	 */
	public static final byte OPTION_STRING = 7;

	/**
	 * Código da opção do tipo boolean.
	 */
	public static final byte OPTION_BOOLEAN = 8;
}
